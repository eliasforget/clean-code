package ex1;

/**
 * Fournir des opérations arithmétiques simples.
 */
public class Calcul {

    /**
     * Additionner deux entiers.
     *
     * @param a premier entier
     * @param b second entier
     * @return somme de a et b
     */
    public int additionner(int a, int b) {
        return a + b;
    }

    /**
     * Soustraire deux entiers.
     *
     * @param a entier de départ
     * @param b entier à soustraire
     * @return différence a - b
     */
    public int soustraire(int a, int b) {
        return a - b;
    }
}
